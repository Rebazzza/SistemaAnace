    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (in.existeProducto(jTextField6.getText().trim())) {
            String tipo = (String) jComboBox1.getSelectedItem();
            if( tipo.equals("Entrada")){
                k.registrarEntradaKardex(jTextField6.getText().trim(), Integer.parseInt(jTextField5.getText().trim()), p.getPrecio(), jDateChooser1.getDate(), jTextField2.getText().trim(),jTextField3.getText().trim());
            }else if(tipo.equals("Salida")){
                k.registrarSalidaKardex(jTextField6.getText().trim(), Integer.parseInt(jTextField5.getText().trim()), p.getPrecio(), jDateChooser1.getDate(), jTextField2.getText().trim(),jTextField3.getText().trim());
            }
        } else {
            in.mostrarError("Producto no existente");
        }        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

